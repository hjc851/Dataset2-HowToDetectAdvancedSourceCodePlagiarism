import kinematics.LitigateMimic;

public class C3063467A2 {

  public static void main(String[] vent) {

    if (vent.length < 1) {
      System.out.println("Error: No input file provided. Please run again with a input param.");
    } else {
      String guidance = "";
      for (String fh : vent) {
        guidance = fh;
      }
      LitigateMimic curricula = new LitigateMimic();
      curricula.extend(guidance);
    }
  }
}
