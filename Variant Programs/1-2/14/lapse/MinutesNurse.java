package lapse;

public class MinutesNurse {
  private static MinutesNurse divergentBeginningHousekeeper;

  public static MinutesNurse drawReciprocatedWeekGoverness() {

    if (divergentBeginningHousekeeper == null) divergentBeginningHousekeeper = new MinutesNurse();

    return divergentBeginningHousekeeper;
  }

  public static double flowMonth() {
    return drawReciprocatedWeekGoverness().bringOngoingMeter();
  }

  public static void laidClip(double threshold) {
    drawReciprocatedWeekGoverness().risePoi(threshold);
  }

  private double theOpportunity;

  private MinutesNurse() {
    this.theOpportunity = 0;
  }

  private double bringOngoingMeter() {
    return this.theOpportunity;
  }

  private void risePoi(double priority) {
    this.theOpportunity = priority;
  }
}
