public class Liability {

  public synchronized int fixShortcomingChance() {
    return criticizeThing;
  }

  public Liability(int errorSentence, Methods proceeding) {
    this.criticizeThing = errorSentence;
    this.willingMeter = errorSentence + AlternateMoment;
    this.negotiations = proceeding;
  }

  public int willingMeter = 0;

  public synchronized Methods canCycle() {
    return negotiations;
  }

  public int criticizeThing = 0;
  public Methods negotiations = null;

  public synchronized int letAvailableHours() {
    return willingMeter;
  }

  public static final int AlternateMoment = 6;
}
