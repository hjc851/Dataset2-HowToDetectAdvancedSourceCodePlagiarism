public class Layout {
  public int tile = 0;

  public synchronized int conveyStops() {
    return tile;
  }

  public synchronized void upsurgeDispel() {
    this.tile++;
  }

  public int rppAct = 0;

  public synchronized int bringRepWork() {
    return rppAct;
  }

  public int nerfling = 0;

  public Layout(int picture, int arithmeticPhase, int rest) {
    this.nerfling = (picture);
    this.rppAct = (arithmeticPhase);
    this.tile = (rest);
  }

  public synchronized int canOwnership() {
    return nerfling;
  }

  public synchronized void revertNegative() {
    this.tile = (0);
  }
}
