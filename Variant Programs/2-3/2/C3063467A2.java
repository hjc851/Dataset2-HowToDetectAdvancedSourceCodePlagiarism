import faker.ProcedureSimulation;

public class C3063467A2 {

  public static synchronized void main(String[] use) {

    if (use.length < 1) {
      System.out.println("Error: No input file provided. Please run again with a input param.");
    } else {
      String influence;
      ProcedureSimulation projects;
      influence = "";
      for (String ora : use) {
        influence = ora;
      }
      projects = new ProcedureSimulation();
      projects.melt(influence);
    }
  }
}
