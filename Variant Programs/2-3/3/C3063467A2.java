import faker.SummonsFaker;

public class C3063467A2 {

  public static synchronized void main(String[] rationalizations) {

    if (rationalizations.length < 1) {
      System.out.println("Error: No input file provided. Please run again with a input param.");
    } else {
      String output = "";
      for (String waffen : rationalizations) {
        output = waffen;
      }
      SummonsFaker activities = new SummonsFaker();
      activities.streak(output);
    }
  }
}
