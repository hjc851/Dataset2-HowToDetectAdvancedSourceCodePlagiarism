import analogue.LitigateMimic;

public class C3063467A2 {

  public static synchronized void main(String[] use) {

    if (use.length < 1) {
      System.out.println("Error: No input file provided. Please run again with a input param.");
    } else {
      String influence;
      LitigateMimic curricula;
      influence = "";
      for (String fh : use) {
        influence = fh;
      }
      curricula = new LitigateMimic();
      curricula.streak(influence);
    }
  }
}
