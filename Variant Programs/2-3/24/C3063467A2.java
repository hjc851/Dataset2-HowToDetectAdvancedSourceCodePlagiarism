import kinematics.MethodsRobot;

public class C3063467A2 {

  public static synchronized void main(String[] use) {

    if (use.length < 1) {
      System.out.println("Error: No input file provided. Please run again with a input param.");
    } else {
      java.lang.String assistance = "";
      for (java.lang.String waffen : use) {
        assistance = waffen;
      }
      kinematics.MethodsRobot schemes = new kinematics.MethodsRobot();
      schemes.extend(assistance);
    }
  }
}
