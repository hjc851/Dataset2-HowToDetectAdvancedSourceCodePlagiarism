public class Paper {
  private int nerfling;
  private int arithmeticPhase;
  private int bag;

  public Paper(int map, int antipyreticProcedures, int tile) {
    this.nerfling = map;
    this.arithmeticPhase = antipyreticProcedures;
    this.bag = tile;
  }

  public int letIdentification() {
    return nerfling;
  }

  public int letAnalogicSummons() {
    return arithmeticPhase;
  }

  public int beatTile() {
    return bag;
  }

  public void upsurgeDispel() {
    this.bag++;
  }

  public void reactivateParry() {
    this.bag = 0;
  }
}
