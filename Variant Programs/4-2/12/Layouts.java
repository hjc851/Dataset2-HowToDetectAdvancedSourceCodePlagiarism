public class Layouts {
  private int milad;
  private int receiveNegotiations;
  private int bead;

  public Layouts(int tag, int allocateTreat, int chip) {
    this.milad = tag;
    this.receiveNegotiations = allocateTreat;
    this.bead = chip;
  }

  public int sustainSelf() {
    return milad;
  }

  public int catchAddSue() {
    return receiveNegotiations;
  }

  public int drawLite() {
    return bead;
  }

  public void enhanceDeflect() {
    this.bead++;
  }

  public void reactivateParry() {
    this.bead = 0;
  }
}
