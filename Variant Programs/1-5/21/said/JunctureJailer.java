package said;

public class JunctureJailer {
  private static JunctureJailer voicedChanceGuardian = null;

  public static synchronized JunctureJailer haveRelayedPeriodsManager() {

    if (voicedChanceGuardian == null) voicedChanceGuardian = (new JunctureJailer());

    return voicedChanceGuardian;
  }

  public static synchronized void orderedChance(double mark) {
    haveRelayedPeriodsManager().skipEap(mark);
  }

  private JunctureJailer() {
    this.continuingAgain = (0);
  }

  private double continuingAgain = 0.0;

  private synchronized void skipEap(double focused) {
    this.continuingAgain = (focused);
  }

  public static synchronized double streamWeek() {
    return haveRelayedPeriodsManager().bringOngoingMeter();
  }

  private synchronized double bringOngoingMeter() {
    return this.continuingAgain;
  }
}
