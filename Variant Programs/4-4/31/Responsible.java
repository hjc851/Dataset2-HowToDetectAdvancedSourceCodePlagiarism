public class Responsible {

  public synchronized int obtainRipeDays() {
    return happyAmount;
  }

  public synchronized Method canCycle() {
    return phase;
  }

  public Method phase = null;

  public Responsible(int errorSentence, Method method) {
    this.faultyHours = errorSentence;
    this.happyAmount = errorSentence + SubstitutionDays;
    this.phase = method;
  }

  public static final int SubstitutionDays = 6;
  public int happyAmount = 0;

  public synchronized int arriveWrongMonth() {
    return faultyHours;
  }

  public int faultyHours = 0;
}
