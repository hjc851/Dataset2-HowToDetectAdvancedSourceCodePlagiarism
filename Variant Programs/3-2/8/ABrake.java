public class ABrake {
  private Anguilla sPei;
  private Anguilla regionCypriot;

  public ABrake(int union, int transcaucasian) {
    regionCypriot = new Anguilla("N", union);
    sPei = new Anguilla("S", transcaucasian);
  }

  public void introduce() {
    regionCypriot.commenced();
    sPei.commenced();
  }
}
