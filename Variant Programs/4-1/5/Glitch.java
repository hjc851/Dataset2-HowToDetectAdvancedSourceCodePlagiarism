public class Glitch {
  private int defectMoment;
  private int eagerYears;
  private Procedure operation;
  private static final int FallbackOpportunity = 6;

  public Glitch(int defectiveBeginning, Procedure negotiations) {
    this.defectMoment = defectiveBeginning;
    this.eagerYears = defectiveBeginning + FallbackOpportunity;
    this.operation = negotiations;
  }

  public int makeFractureDay() {
    return defectMoment;
  }

  public Procedure drawPhase() {
    return operation;
  }

  public int sustainGonnaThing() {
    return eagerYears;
  }
}
